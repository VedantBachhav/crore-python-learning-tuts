# Typecasting : The conversion between one data type to another data type is known as typecasting.
# There are two types of typecasting :
    # 1) Implicit typecasting (conversion) : The conversion between one datatype to anoter done automatically is known as implicit typecasting.
    # example:
a = 10
b = 55

print(a + b)

    # 2) Explicit typecasting (conversion) : The conversion between one datatype to anoter done via develpoer is known as explicit typecasting.

a = 10
b = 55

print(a + b)

# or

a = "50"
b = "55"
c = 5.5
print(int(a) + int(b))
print(type(c))